# Define the variable "Grade" with an integer value between 0 to 100
Grade = 85

# Print the letter grade corresponding to the number grade
if Grade >= 90:
    print("The letter grade is: A")
elif Grade >= 80:
    print("The letter grade is: B")
elif Grade >= 70:
    print("The letter grade is: C")
elif Grade >= 60:
    print("The letter grade is: D")
else:
    print("The letter grade is: F")